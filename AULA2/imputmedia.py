# Faça um programa que solicite ao usuário os valores das 4 notas do bimestre. E calcule o valor da média.
# int(input(""))
# Resultado esperado: 
# o valor da média é: 00
# 01/04

nota1 = int(input("digite a primeira nota:"))
nota2 = int(input("digite a segunda nota:"))    # int = inteiro
nota3 = int(input("digite a terceira nota:"))   # float = numeros com virgula
nota4 = int(input("digite a quarta nota:"))
media = (nota1+nota2+nota3+nota4)/4
print("A sua média é:", media)
